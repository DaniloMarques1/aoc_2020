package org.example;

public class Slope {
    private final int right;
    private final int down;

    public Slope(int right, int down) {
        this.right = right;
        this.down = down;
    }
    
    public int getRight() {
        return this.right;
    }
    
    public int getDown() {
        return this.down;
    }

}
